print("normal distribution")
x=seq(2,4,by=0.4)
print(x)
mean(x)
sd(x)
dnorm(x,m=3,s=1)
plot(dnorm(x,m=3,s=1))
pnorm(x,mean=0,sd=3)
plot(pnorm(x,m=0,s=3))
qnorm(x,mean=0,sd=3)
plot(qnorm,mean=0,sd=3)


